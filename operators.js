// operators in JS

//Arithmetic operators
let a = 12
let b = 4

console.log("a + b=",a+b)
console.log("a - b=",a-b)
console.log("a * b=",a*b)
console.log("a / b=",a/b)
console.log("a ** b=",a**b)
console.log("a % b=",a%b)
console.log("a ++ =",a++)
console.log("a -- =",a--)

//Assignment operators
c = 8
c += 3
console.log(c)

//comparison operators
comp1 = 12
comp2 = 10
console.log("comp1 == comp2 is", comp1 == comp2)
console.log("comp1 == comp2 is", comp1 != comp2)
console.log("comp1 === comp2 is", comp1 === comp2)
console.log("comp1 !== comp2 is", comp1 !== comp2)
